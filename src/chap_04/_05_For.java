package chap_04;

public class _05_For {
    public static void main(String[] args) {
        // 반복문 for
        for (int i =0 ;i<10;i++){ // c와 동일
            System.out.println("siuuuuu");
        }

        for (int i =0 ;i<10;i+=2){ // c와 동일
            System.out.print(i); // print는 한줄로 이어서 출력 가능
        }
    }
}
