package chap_02;

public class _02_Operator2 {
    public static void main(String[] args) {
        // 복합 대입 연산자
        int n=10;
        n+=2;
        System.out.println(n);

        n=10;
        n-=2;
        System.out.println(n);

        n=10;
        n*=2;
        System.out.println(n);

        n=10;
        n/=2;
        System.out.println(n);

        n=10;
        n%=2;
        System.out.println(n);
    }
}
